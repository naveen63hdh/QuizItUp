package com.example.quizitup.classroom.RequestParticipant;

public class RequestParticipant {
    String uid, name;

    public RequestParticipant(String uid, String name) {
        this.uid = uid;
        this.name = name;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
